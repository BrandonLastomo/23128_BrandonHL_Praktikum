public class Matematika {
    // method untuk menjumlahkan dua nilai dan menampilkannya
    public void pertambahan(int a, int b){
        System.out.println("Pertambahan: " + a + " + " + b + " = " + (a+b));
    }
    // method untuk mencari selisih dari dua nilai dan menampilkannya
    public void pengurangan(int a, int b){
        System.out.println("Pengurangan: " + a + " - " + b + " = " + (a-b));
    }
    // method untuk mengalikan dua nilai antara satu dengan lainnya dan menampilkannya
    public void perkalian(int a, int b){
        System.out.println("Perkalian: " + a + " * " + b + " = " + (a*b));
    }
    // method untuk membagi dua nilai antara satu dengan lainnya dan menampilkannya
    public void pembagian(int a, int b){
        System.out.println("Pembagian: " + a + " / " + b + " = " + (a/b));
    }
}
