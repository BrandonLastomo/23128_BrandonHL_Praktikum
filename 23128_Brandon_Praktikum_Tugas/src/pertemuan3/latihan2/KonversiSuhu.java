public class KonversiSuhu {
    protected void celciusToFahrenheit(double suhu){
        System.out.println("Nilai Fahrenheit dari " + suhu + " C" + " adalah " + ((suhu*9/5)+32));
    }
    protected void celciusToReamur(double suhu){
        System.out.println("Nilai Reamur dari " + suhu + " C" + " adalah " + (suhu*4/5));
    }
}
