public class Matematika {
    public void tambah(int a, int b){
        System.out.println("Hasil pertambahan dari " + a + "+" + b +" adalah " + (a+b));
    }
    public void kurang(int a, int b){
        System.out.println("Hasil pengurangan dari " + a + "-" + b +" adalah " + (a-b));
    }
    public void kali(int a, int b){
        System.out.println("Hasil perkalian dari " + a + "*" + b +" adalah " + (a*b));
    }
    public void bagi(int a, int b){
        System.out.println("Hasil pembagian dari " + a + "/" + b +" adalah " + (a/b));
    }
}